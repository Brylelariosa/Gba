/* ── Init ────────────────────────────────────────── */
void renderer_init(void) {
    /* Mode 4: 240x160 palettised, BG2 */
    SetMode(MODE_4 | BG2_ENABLE);
    memset(FB0, PAL_DEAD, SCREEN_W * SCREEN_H);
    memset(FB1, PAL_DEAD, SCREEN_W * SCREEN_H);
}

void renderer_vsync(void) { VBlankIntrWait(); }

void renderer_flip(void) {
    if (s_draw_buf == FB0) {
        SetMode(MODE_4 | BG2_ENABLE);        /* show FB0 */
        s_draw_buf = FB1;
        s_show_buf = FB0;
    } else {
        SetMode(MODE_4 | BG2_ENABLE | BACKBUFFER); /* show FB1 */
        s_draw_buf = FB0;
        s_show_buf = FB1;
    }
    (void)s_show_buf;
}

/* ── Pixel helpers ───────────────────────────────── */
static inline void set_pixel(int x, int y, u8 col) {
    if ((u32)x < SCREEN_W && (u32)y < SCREEN_H) {
        /* Mode 4: 8bpp, two pixels per halfword, stored LE */
        u16 *hw = (u16 *)s_draw_buf + (y * SCREEN_W + x) / 2;
        if (x & 1) *hw = (*hw & 0x00FF) | ((u16)col << 8);
        else        *hw = (*hw & 0xFF00) | col;
    }
}

/* Fast 2-pixel-wide horizontal span */
static inline void hline2(int x, int y, int w, u8 col) {
    u16 *dst = (u16 *)s_draw_buf + y * (SCREEN_W / 2) + x / 2;
    u16 c2   = (u16)(col | (col << 8));
    int n    = w / 2;
    while (n--) *dst++ = c2;
}

/* ── Grid drawing ────────────────────────────────── */
void renderer_draw_grid(void) {
    for (int y = 0; y < GRID_H; y++) {
        const u8 *row  = g_sim.cur[y];
        int        py   = y * CELL_PX;
        u16       *ln0  = (u16 *)s_draw_buf + (py)     * (SCREEN_W / 2);
        u16       *ln1  = (u16 *)s_draw_buf + (py + 1) * (SCREEN_W / 2);
        for (int x = 0; x < GRID_W; x++) {
            u8 cell = row[x];
            u8 col;
            switch (g_sim.type) {
                case AUTO_WIREWORLD:
                    col = cell == WW_EMPTY ? PAL_DEAD    :
                          cell == WW_WIRE  ? PAL_STATE3  :
                          cell == WW_HEAD  ? PAL_ALIVE   :
                                            PAL_STATE2;
                    break;
                case AUTO_BRIANS_BRAIN:
                    col = cell == BB_OFF   ? PAL_DEAD   :
                          cell == BB_ON    ? PAL_ALIVE  :
                                            PAL_STATE2;
                    break;
                case AUTO_CYCLIC:
                    col = cell < 16 ? (u8)(PAL_CYC0 + cell) : PAL_ALIVE;
                    break;
                default:
                    col = cell ? PAL_ALIVE : PAL_DEAD;
                    break;
            }
            int px = x * CELL_PX;
            /* Write two consecutive bytes per cell row (packed 8bpp) */
            u16 cv = (u16)(col | (col << 8));
            ln0[px / 2] = cv;
            ln1[px / 2] = cv;
        }
    }
}

/* ── Cursor ──────────────────────────────────────── */
void renderer_draw_cursor(int x, int y) {
    int px = x * CELL_PX, py = y * CELL_PX;
    set_pixel(px,     py,     PAL_CURSOR);
    set_pixel(px + 1, py,     PAL_CURSOR);
    set_pixel(px,     py + 1, PAL_CURSOR);
    set_pixel(px + 1, py + 1, PAL_CURSOR);
}

/* ── Fill rect ───────────────────────────────────── */
void renderer_fill_rect(int x, int y, int w, int h, u8 col) {
    for (int row = 0; row < h; row++)
        for (int cx = x; cx < x + w; cx++)
            set_pixel(cx, y + row, col);
}

/* ── Character drawing ───────────────────────────── */
void renderer_draw_char(int x, int y, char c, u8 col) {
    if (c < 32 || c > 127) c = '?';
    const u8 *glyph = FONT[(u8)c - 32];
    for (int row = 0; row < 6; row++) {
        u8 bits = glyph[row];
        for (int col2 = 0; col2 < 5; col2++) {
            if (bits & (0x10 >> col2))
                set_pixel(x + col2, y + row, col);
        }
    }
}

void renderer_draw_string(int x, int y, const char *s, u8 col) {
    int cx = x;
    while (*s) {
        renderer_draw_char(cx, y, *s++, col);
        cx += CHAR_W + 1;
    }
}

/* ── Number drawing ──────────────────────────────── */
void renderer_draw_uint(int x, int y, u32 n, u8 col) {
    char buf[12];
    int pos = 11; buf[pos] = '\0';
    if (n == 0) { buf[--pos] = '0'; }
    else {
        u32 v = n;
        while (v) { buf[--pos] = (char)('0' + v % 10); v /= 10; }
    }
    renderer_draw_string(x, y, &buf[pos], col);
}

void renderer_draw_int(int x, int y, int n, u8 col) {
    if (n < 0) {
        renderer_draw_char(x, y, '-', col);
        renderer_draw_uint(x + CHAR_W + 1, y, (u32)(-n), col);
    } else {
        renderer_draw_uint(x, y, (u32)n, col);
    }
}

/* ── Status bar ──────────────────────────────────── */
void renderer_draw_status(void) {
    /* Fill status area */
    renderer_fill_rect(0, STATUS_Y, SCREEN_W, STATUS_H, PAL_STAT_BG);

    /* Line 1: automata name + gen */
    char buf[32];
    extern const char *RULE_NAMES[];
    renderer_draw_string(2, STATUS_Y + 2,
        RULE_NAMES[g_sim.type], PAL_STAT_TX);

    /* Generation */
    renderer_draw_string(90, STATUS_Y + 2, "G:", PAL_STAT_TX);
    renderer_draw_uint(104, STATUS_Y + 2, g_sim.generation, PAL_STAT_TX);

    /* Population */
    renderer_draw_string(155, STATUS_Y + 2, "P:", PAL_STAT_TX);
    renderer_draw_uint(169, STATUS_Y + 2, g_sim.population, PAL_STAT_TX);

    /* Paused indicator */
    if (g_sim.paused)
        renderer_draw_string(220, STATUS_Y + 2, "||", PAL_RED);

    /* Line 2: B/S rule */
    extern void rule_get_bs_string(char *, int);
    rule_get_bs_string(buf, sizeof(buf));
    renderer_draw_string(2, STATUS_Y + 11, buf, PAL_DARK);

    /* Cursor pos */
    renderer_draw_string(80, STATUS_Y + 11, "X:", PAL_DARK);
    renderer_draw_int(94, STATUS_Y + 11, g_sim.cur_x, PAL_DARK);
    renderer_draw_string(120, STATUS_Y + 11, "Y:", PAL_DARK);
    renderer_draw_int(134, STATUS_Y + 11, g_sim.cur_y, PAL_DARK);

    /* Births/Deaths */
    renderer_draw_string(160, STATUS_Y + 11, "B:", PAL_CYAN);
    renderer_draw_uint(174, STATUS_Y + 11, g_sim.births, PAL_CYAN);
    renderer_draw_string(200, STATUS_Y + 11, "D:", PAL_RED);
    renderer_draw_uint(214, STATUS_Y + 11, g_sim.deaths, PAL_RED);
}